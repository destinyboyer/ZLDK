void Customer::setData(istream & inFile)
{
	inFile >> this->ID >> this->lastName >> this->firstName;
}

void Customer::displayCustomer(void) const
{
	cout << "Customer ID: " << this->getID() << " First: " << this->firstName << " Last: " << this->lastName << endl;
}



